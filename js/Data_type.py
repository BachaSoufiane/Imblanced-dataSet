
# type() is a function that ca use to know the data type of variables
print(type(10)) #data type =>Int:Integer
print(type(-10)) #data type =>Int:Integer
print(type(10.10)) #data type =>Float:Float
print(type(-10.10)) #data type =>Float:Float
print(type("I love python")) #data type =>Str:String
print(type([1,2,3,4,5])) #data type =>List
print(type((1,2,3,4,5))) #data type =>tuple
print(type({"A":1,"B":2,"C":3})) #data type =>Dic:dictionary
print(type(2==2)) #data type =>bool:boolean